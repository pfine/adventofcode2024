"""Module providing a function printing python version."""

def part_1(input_data: list[str]) -> None:
    """Function printing python version."""
    lines = input_data
    lines = [line.strip() for line in lines]
    print("Lines = ", lines)

    for line in lines:
        print(" > line = ", line)

    print("Hello World! part_2")

